// var significa variável, porém nesse caso ela pode ser usada fora do bloco de programação
var nome = "Nicole";
console.log(nome);
// let significa variável, porém só pode ser utilizado dentro do bloco em que foi declarado
let idade = 17;
console.log(idade);
// const significa uma constante dentro do codigo
const pi = 3.14;
console.log(pi);